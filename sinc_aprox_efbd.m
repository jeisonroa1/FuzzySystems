clear;
clc;


med = [-1.4586995  -1.0379004  1.0722471  3.9665455  1.8959475  9.4079031  -4.8181421  10.670267  1.0317836  3.5502716  6.698333  -0.6592127 -3.5918029  -6.811673 5.0170119  -2.5740792]';

dev = [4.6986485 9.5208871 0.0687298 1.4315971 9.1840547 8.8427337 12.286935 5.8090189 11.689256 0.6032973 6.5921137 1.3547512 2.1373191 10.853332 8.1770166 4.3938402]';

y = [  6.0736231 0.2121900 1.152005 -6.0007557  -6.6068075 -1.979804 -3.9777274 0.2401398 -3.8976428 0.3594843 4.0102735 4.6313348 -9.3702991 5.0795487 6.538603 6.1882048]';


u=-10:0.5:10;
for k=1:length(u),
x=u(1,k);
m=length(y);
n=length(x);
z=1;
for i=1:m,
  for j=1:n,
    xp=x(1,j);
    mp=med(i,j);
    dp=dev(i,j);
    f=exp((-0.5*(xp-mp)^2)/(dp^2));
    z=z*f;
  end;
  rul(i,1)=z;
  z=1;
end;
b=sum(rul);

for q=1:m,
  fbd(q,k)=rul(q,1)/b;
end
end;

for p=1:m,
  rw(p,:)=y(p,1)*fbd(p,:);
end;

plot(u,fbd);

figure

plot(u,rw);

efbd=sum(rw,1);

figure

fy=sinc(u./pi);

plot(u,efbd);
hold
plot(u,fy,'r');



